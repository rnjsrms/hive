// checkPrerequisites
// ---------------------------------------------------------------------------

describe('checkPrerequisites', () => {
  it('returns ready when gh and remote are available', () => {
    const result = checkPrerequisites(true, true);
    expect(result.ready).toBe(true);
    expect(result.missing).toEqual([]);
  });

  it('reports missing gh CLI', () => {
    const result = checkPrerequisites(false, true);
    expect(result.ready).toBe(false);
    expect(result.missing).toHaveLength(1);
    expect(result.missing[0]).toContain('gh CLI');
  });

  it('reports missing remote', () => {
    const result = checkPrerequisites(true, false);
    expect(result.ready).toBe(false);
    expect(result.missing).toHaveLength(1);
    expect(result.missing[0]).toContain('remote');
  });

  it('reports both missing', () => {
    const result = checkPrerequisites(false, false);
    expect(result.ready).toBe(false);
    expect(result.missing).toHaveLength(2);
  });
});
